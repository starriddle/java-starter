class Solution {
    public int reverse(int x) {
        long from = x, to = 0;
        int flag = 1;
        if (from<0) {
            from = -from;
            flag = -flag;
        }
        do {
            long num = from % 10;
            from /= 10;
            to = to*10 + num;
        } while (from != 0);
        to *= flag;
        if (to > Integer.MAX_VALUE || to < Integer.MIN_VALUE) {
            to = 0;
        }
        return (int)to;
    }
}
